# งบประมาณ
budget = 1000.00

# คำนวณราคารวม
total_cost = sum(items.values())

# คำนวณเงินทอน
change = budget - total_cost

# แสดงผลลัพธ์
print("=== รายการสินค้า ===")
for name, price in items.items():
    print(f"{name}: {price:.2f} บาท")

print("\n=== สรุปผล ===")
print(f"จำนวนสินค้าที่ซื้อ: {len(items)} รายการ")
print(f"ราคารวมทั้งหมด: {total_cost:.2f} บาท")
